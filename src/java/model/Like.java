package model;

/**
 *
 * @author thaoq
 */
public class Like {
    String uid;
    String pid;

    public Like() {
    }

    public Like(String uid, String pid) {
        this.uid = uid;
        this.pid = pid;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }
    
    
    
}
